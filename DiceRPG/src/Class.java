public abstract class Class {
    private String className;
    private int health;
    private int attack;
    private int defense;
    private int power;
    private int speed;
    private Weapon weapon;

    public String getClassName() {
        return className;
    }

    public int getHealth() {
        return health;
    }

    public int getAttack() {
        return attack;
    }

    public int getDefense() {
        return defense;
    }

    public int getPower() {
        return power;
    }

    public int getSpeed() {
        return speed;
    }
}
